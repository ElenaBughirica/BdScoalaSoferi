    public void update(){
    try {
            comunicatie cm=new comunicatie();
            
            ResultSet rs=cm.exQuery("SELECT * FROM Masina");
            //stergere rezultate anterioare
            while(model.getRowCount()>0){
            model.removeRow(0);
            }
            
            TableColumn id=jTable1.getColumn("IDMasina");
            id.setMaxWidth(0);
            id.setMinWidth(0);
            id.setPreferredWidth(0);
            while(rs.next()) {
                model.insertRow(model.getRowCount(),new Object[]{
                rs.getString("IDMasina"),rs.getString("Numar"),rs.getString("KmBord"),rs.getString("Marca"),
                    });
            }
            cm.closeChannel(rs);
        } catch (SQLException ex) {
            Logger.getLogger(ScoalaSoferi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
